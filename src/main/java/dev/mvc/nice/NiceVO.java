package dev.mvc.nice;

public class NiceVO {
  private int niceno;
  private int reviewno;
  private int memno;
  private String rdate;
  public int getNiceno() {
    return niceno;
  }
  public void getNiceno(int niceno) {
    this.niceno = niceno;
  }
  public int getReviewno() {
    return reviewno;
  }
  public void setReviewno(int reviewno) {
    this.reviewno = reviewno;
  }
  public int getMemno() {
    return memno;
  }
  public void setMemno(int memno) {
    this.memno = memno;
  }
  public String getRdate() {
    return rdate;
  }
  public void setRdate(String rdate) {
    this.rdate = rdate;
  }
  
  
}
